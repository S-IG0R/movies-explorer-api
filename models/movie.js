const mongoose = require('mongoose');
const urlChecker = require('../utils/constants');

const movieSchema = new mongoose.Schema(
  {
    country: {
      type: String,
      required: true,
    },
    director: {
      type: String,
      required: true,
    },
    duration: {
      type: Number,
      required: true,
    },
    year: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
      validate: urlChecker,
    },
    trailerLink: {
      type: String,
      required: true,
      validate: urlChecker,
    },
    thumbnail: {
      type: String,
      required: true,
      validate: urlChecker,
    },
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'user',
      required: true,
    },
    movieId: {
      type: Number,
      required: true,
    },
    nameRU: {
      type: String,
      required: true,
    },
    nameEN: {
      type: String,
      required: true,
    },
  },
  { versionKey: false }, // убирает версию
);

module.exports = mongoose.model('movie', movieSchema);
